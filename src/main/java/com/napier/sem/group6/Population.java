package com.napier.sem.group6;

public class Population {

    String name;
    long totalPopulation;
    int populationLivingInCity;
    int populationNotInCity;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public long getTotalPopulation() {
        return totalPopulation;
    }

    public void setTotalPopulation(long totalPopulation) {
        this.totalPopulation = totalPopulation;
    }

    public int getPopulationLivingInCity() {
        return populationLivingInCity;
    }

    public void setPopulationLivingInCity(int populationLivingInCity) {
        this.populationLivingInCity = populationLivingInCity;
    }

    public int getPopulationNotInCity() {
        return populationNotInCity;
    }

    public void setPopulationNotInCity(int populationNotInCity) {
        this.populationNotInCity = populationNotInCity;
    }





}
